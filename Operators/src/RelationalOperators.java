public class RelationalOperators {
    public static void main(String[] args) {
        int p = 30, q = 60;

        System.out.println(p == q); //false
        System.out.println(p != q);
        System.out.println(p <= q);
        System.out.println(p >= q);
        System.out.println(p <q);
        System.out.println(p >q);
    }
}
